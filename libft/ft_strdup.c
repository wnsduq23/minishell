#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	int		len;
	char	*ret;

	len = ft_strlen((char *)str);
	ret = (char *)malloc(sizeof(char) * (len + 1));
	if (!ret)
		return (NULL);
	i = 0;
	while (str[i])
	{
		ret[i] = str[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
